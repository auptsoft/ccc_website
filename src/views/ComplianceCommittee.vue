            <li>
              The Comptroller General of Customs as the grand Chairman, the incorporated Board
              members, nominated and appointed representatives.
            </li>
            <li>
              For the exigencies of official demands, the Comptroller General Of Customs shall
              appoint a Vice Chairman to represent him in all meetings.
            </li>
            <li>
              The Comptroller General of Customs in Consultation with the incorporated Board, shall
              official appointed Secretary to the Committee.
            </li>
            <li>
              The Representing Vice Chairman shall be a Principal Revenue and Technical Officers,
              responsible for customs issues, to be accompanied by a staff dealing with the any
              required items on the agenda before a particular meeting.
            </li>
            <li>
              Where issues involves a particular Organisations - suhc organization can be atrended
              to provided that, it member's ( firm) have a regular and direct dealings with Customs.
            </li>
          </ul>
