        <h4 class="font-bold mb-1">Uncleared goods</h4>
        <p class="mb-1">
          Art. 60 is very relevant and impacts terminal operators significantly through the
          management of uncleared and missing goods. Here's how:
        </p>
        <ul class="list-disc pl-10">
          <li class="mb-1">
            Timely Transfer to Government Warehouse: On the 30th day after the completion of
            discharge of the importing ship, aircraft, or vehicle, the proper officer will provide a
            list of goods that have been unloaded but not yet released. Terminal operators must
            immediately transfer these goods to a Government warehouse or another approved place.
          </li>
        </ul>
        <p class="mb-1">
          Art. 119 specifically addresses the disposal of uncleared goods that have been moved to a
          Government warehouse:
        </p>
        <ul class="list-disc pl-10">
          <li class="mb-1">
            Immediate Action for Perishable Goods: If the goods are perishable, they must be
            disposed of immediately. The Service has the discretion to determine which goods are
            perishable and require prompt disposal
          </li>
          <li class="mb-1">
            Fourteen-Day Rule for Non-Perishable Goods: For non-perishable goods, the importer has
            14 days after the goods have been moved to the Government warehouse to clear them. If
            the goods are not cleared within this period, the Service may sell them. The Service may
            extend this period at its discretion
          </li>
          <li class="mb-1">
            Public Auction or Tender: Goods that are not cleared within the stipulated time may be
            sold by public auction or tender. The auction dates are publicized in advance through
            national newspapers, television, and the Service's website to ensure transparency. The
            auction procedures are established by the Board
          </li>
          <li class="mb-1">
            Costs and Proceeds: The proceeds from the sale are used to cover any unpaid duties,
            storage fees, and other related costs. Any remaining balance after covering these costs
            may be refunded to the importer if they come forward to claim it within a specified
            period
          </li>
        </ul>
        <p class="mb-1">
          The provisions of Art. 60 and 119 on the transfer and disposal of uncleared goods are
          cause of following concern for Terminal Operators:
        </p>
        <p class="mb-1">30-Day Period for Transfer to Government Warehouse (Article 60):</p>
        <ul>
          <li class="mb-1">
            Concern: The average dwell time for containers in Lagos being 18 days means there is
            little buffer for delays, which are common due to various logistical, administrative, or
            regulatory issues. Over one third of containers discharged are cleared after 30 days:
          </li>
          <li class="mb-1">
            Implication: This tight deadline can lead to operational bottlenecks and increased costs
            for terminal operators and importer
          </li>
        </ul>
        <p class="mb-1">14-Day Rule for Clearance from Government Warehouse (Article 119)</p>
        <ul>
          <li class="mb-1">
            Concern: This period may be insufficient for importers to arrange for clearance,
            especially if they were already facing delays that led to the transfer in the first
            place.
          </li>
        </ul>
        <p class="mb-1">Use of Proceeds from Sale (Article 119)</p>
        <ul>
          <li class="mb-1">
