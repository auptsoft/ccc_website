        <h4 class="font-bold mb-2">Customs Service Control</h4>
        <p class="mb-1">
          Art. 30 of the Nigeria Customs Act, 2023, outlines the responsibilities and powers of the
          Nigeria Customs Service regarding customs control. This provision is relevant for terminal
          operators in several key ways:
        </p>
        <ul class="list-disc pl-10">
          <li class="mb-1">
            Goods Examination: The Service has the authority to examine goods to ensure compliance
            with customs regulations. For terminal operators, this means providing access and
            facilities for customs officers to conduct these examinations efficiently
          </li>
          <li class="mb-1">
            Inspection of Transport Means: Customs officers are authorized to inspect the means of
            transport used for moving goods. Terminal operators must facilitate these inspections
            and ensure that all transport vehicles meet the necessary standards and regulation
          </li>
          <li class="mb-1">
            Sampling and Interviews: The Service may take samples of goods and interview individuals
            who may have relevant information. Terminal operators need to cooperate with these
            activities by providing samples and ensuring that knowledgeable personnel are available
            for interviews
          </li>
        </ul>
        <p class="mb-1">
          Art. 32 provides for the application of risk management using electronic data processing
          technique before examining goods.
        </p>
        <ul class="list-disc pl-10">
          <li class="mb-1">
            This is an essential improvement on CEMA act 2004 as it is expected to reduce the number
            of containers which are physically examined
          </li>
        </ul>
        <p class="mb-1">
          Art. 33 emphasizes the coordination and efficiency of control measures involving multiple
          agencies. This is critical for Terminals. Here's how:
        </p>
        <ul class="list-disc pl-10">
          <li class="mb-1">
            <span class="font-bold"> Simultaneous Control Measures: </span>
            <span
              >The Service is required to ensure that control measures within customs control zones
              and those by other agencies on goods to be imported or exported are carried out
              simultaneously.
            </span>
            <span class="text-red-600"
              >This provision is crucial for terminal operators as it reduces the time needed for
              various inspections, thereby speeding up the clearance and handling of goods</span
            >
          </li>
          <li class="mb-1">
            <span class="font-bold">Coordination of Inspections: </span>
            <span
              >The Service coordinates the location and timing of inspections and other controls
