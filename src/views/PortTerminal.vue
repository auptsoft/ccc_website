            </span>
            <span class="text-red-600"
              >This coordination minimizes disruptions and allows terminal operators to better plan
              their operations and resource allocation</span
            >
          </li>
          <li class="mb-1">
            <span class="font-bold">Electronic Data Exchange Facility (Single Window): </span>
            <span
