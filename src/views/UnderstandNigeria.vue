          4.0. HIGHLIGHT OF NCS ACT 2023 REGULATORY PROVISIONS IN RELATION TO SMUGGLING.
        </h2>
        <p class="font-bold text-red-600">
          4.1. Definition Of Smuggling: From the view point of the Customs definition, smuggling can
          be viewed from six headings, which are:
        </p>
        <ol class="list-[upper-roman] pl-5">
          <li>False declaration and concealment.</li>
          <li>Willful under payment of Customs duties.</li>
          <li>Trafficking in prohibited, restricted goods.</li>
          <li>Use of unapproved routes and ports.</li>
          <li>Forging of customs documents, hacking of customs system.</li>
          <li>Touting in Customs goods and documents (mutilation, machine outside faking), etc.</li>
        </ol>
      </div>
    </div>
    <div>
      <div>
        <p class="font-bold text-red-600">
