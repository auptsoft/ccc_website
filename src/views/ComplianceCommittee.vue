          </h4>
          <h4 class="font-bold bg-[#CBE9E1] mb-2">EXPLANATION 2:</h4>
          <p>This can be explained as thus:</p>
          <ol class="list-[lower-alpha] pl-5">
            <li class="mb-2">
              In general, the Customs Consultative Committees are not mandatory, but highly
              recommended bodies.
            </li>
            <li class="mb-2">
              Membership of the CCCs consists of Customs, other government institutions, and
              business organisations whose members have regular and direct dealings with Customs.
            </li>
            <li class="mb-2">
              Meetings are superintendent over by the Chairperson on behalf of the grand Chairperson
              of the CCC, which is the Comptroller General of Customs.
            </li>
            <li class="mb-2">
              The main form of activity of the CCC is regular meetings (three or more times a year).
            </li>
            <li class="mb-2">
              The rules governing the work of CCCs are not identical, but varies from country to
              country, but they are similar in essence.
            </li>
