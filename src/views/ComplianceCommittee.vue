          <h4 class="font-bold">2.2. Procedure For Seeking Membership:</h4>
          <ul class="list-disc pl-5 marker:text-xl">
            <li>
              Organisations seeking membership of the Committee will be required to apply in writing
              to Comptroller General of Customs, outlining the reasons for seeking membership, the
              number of companies that their organisation represents and the proposed nominee(s) to
              serve on the Committee.
            </li>
            <li>
              The Comptroller General Of Customs will seek the views of the existing members of the
              Committee on the proposal to admit a new member.
            </li>
            <li>
              The decision on admission will normally be by agreement but in the absence of
              agreement, the Comptroller General of Customs will make the final decision.
            </li>
          </ul>
        </div>

