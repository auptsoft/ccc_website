        </p>
        <p class="mb-2">
          The CCC OTRT targets to x-ray and reappraise the essentials of the ever increasing global
          trade dynamics, the supply chain management and to mobilize the Nigeria International
          Shippers, Carriers, Customs, Logisticians, Port Operators and regulatory organizations
          towards an even national economic growth, in real term via shared knowledges and
          experiences.
        </p>
        <h4 class="font-bold text-xl mb-2">VISIONS OF THE CCC OTRT:</h4>
        <p class="mb-2">
          The CCC is deeply concern with dynamics and trends with in the international trade and
          security supply chain, especially, from fostering efciency and seamless considerations
          along the Customs ports operations, thus, a need for process and operational reappraisals,
          is apt.
        </p>
        <p class="mb-2">
          The CCC breakfast aims to providing a platform for an intellectual brainstorming sessions,
          resulting to cross fertilization and collation of robust ideas in relation to improving
          trades, shipping, ports, customs and regulatories activities within the supply chains.
        </p>
        <p class="mb-2">
          As a statutory obligation, it will articulate ideas and recommendations for the attention
          of the relevant policy makers in the supply chain.
        </p>
        <p class="mb-2">
          The CCC is committed to profferring ideas and solutions that will aide Customs
          administration positively, by providing a conducive environment for critical stakeholders
          in the transport cum trade industry to interact freely and genuinely.
        </p>
        <p class="mb-2">
          It also aims to persuade stakeholders to embrace the OTRT as an ideal, most preferred and
          perfect business round table to discuss international trade concerns and above all
          creating an enabling meeting point for Business to Customs connect and Operators to
          regulatory authorities connect platform.
        </p>
