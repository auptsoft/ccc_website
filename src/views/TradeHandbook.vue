        <span class="font-bold">- Seal and Authentication:</span>
        <span>Affixing the seal, authentication, and execution of documents.</span>
      </div>
      <h3 class="font-bold pb-2 pt-4">
        4.0. HIGHLIGHT OF NCS ACT 2023 REGULATORY PROVISIONS IN RELATION TO SMUGGLING.
      </h3>
      <div class="pb-3">
        <span class="font-bold">4.1. Definition Of Smuggling:</span>
        <span
          >- From the view point of the Customs definition, smuggling can be viewed from six
          headings, which are:</span
        >
      </div>
      <ol class="list-[upper-roman] pl-5">
        <li>False declaration and concealment.</li>
        <li>Willful under payment of Customs duties.</li>
        <li>Trafficking in prohibited, restricted goods.</li>
        <li>Use of unapproved routes and ports.</li>
        <li>Forging of customs documents, hacking of customs system.</li>
        <li>Touting in Customs goods and documents (mutilation, machine outside faking, etc).</li>
      </ol>
      <p class="py-4">
        In view of this, the Nigeria Customs Service Act 2023 has several regulatory provisions
        related to smuggling, this includes:
      </p>
      <div class="py-2">
        <span class="font-bold">- Prevention of Smuggling: </span>
        <span
          >The Act empowers the Nigerian Customs Service to prevent smuggling, customs fraud, and
          other violations, ensuring border security and protecting the nation's economy.</span
        >
      </div>

      <div class="py-2">
        <span class="font-bold">- Penalties for Smuggling: </span>
        <span
          >The Act outlines penalties for individuals involved in smuggling, including imprisonment
          and forfeiture of smuggled items. Sections 45, 164, 233, 234, and 237 of the Act detail
          these penalties.</span
        >
      </div>

      <div class="py-2">
        <span class="font-bold">- Detention and Seizure:</span>
        <span
          >The Service is authorized to detain, arrest, or seize goods that infringe intellectual
          property rights or are being smuggled into the country (Section 168 of the Act).</span
        >
      </div>
      <div class="py-2">
        <span class="font-bold">- Border Enforcement: </span>
        <span
          >The Act mandates collaboration with relevant agencies to carry out border enforcement and
          regulatory activities, further strengthening anti-smuggling efforts.
        </span>
      </div>
      <p>
        These provisions demonstrate the Act's focus on preventing smuggling and protecting
        Nigeria's economic interests. By empowering the Nigerian Customs Service to effectively
        enforce customs regulations, the Act aims to promote legitimate trade and commerce while
        safeguarding the nation's borders..
      </p>
