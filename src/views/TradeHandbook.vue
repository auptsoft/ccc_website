      <h3 class="font-bold py-3">1.0. AN OVERVIEW OF THE NCS ACT 2023.</h3>
      <p class="py-3">
        The Nigeria Customs Service Act 2023 is a legislation that governs the operations of the
        Nigerian Customs Service, focusing on trade facilitation, revenue collection, and border
        security. Here are some key aspects of the Act:
      </p>
      <h4 class="py-3 font-bold">Key Provisions:</h4>

      <p class="py-3">
        - Establishment and Functions: The Act establishes the Nigerian Customs Service as a legal
        entity, outlining its functions and powers, including administering customs laws, collecting
        revenue, preventing smuggling, and facilitating trade.
      </p>
      <p class="py-3">
        - Governing Board: The Act establishes a Governing Board for the Service, comprising the
        Minister of Finance, Comptroller-General, and representatives from various Federal
        Ministries.
      </p>
      <p class="py-3">
        - Technology Deployment: The Act mandates the use of electronic data processing methods for
        information exchange and provides for the development of electronic systems to fast-track
        customs operations
      </p>
      <div class="py-3">
        <span class="font-bold">- Intellectual Property Protection: </span
        ><span
          >The Act empowers the Service to detain, arrest, or seize goods that infringe on
          intellectual property rights.</span
        >
      </div>
      <div class="py-3">
        <span class="font-bold">- Authorized Economic Operators:</span>
        <span
          >The Act introduces the concept of Authorized Economic Operators, simplifying procedures
          for clearance and certification.</span
        >
      </div>
      <h4 class="py-3 font-bold">Scope and Application:</h4>
      <p>
        - The Act applies to all imports and exports in Nigeria, aiming to facilitate international
        trade while protecting the nation's economy and security.
      </p>
      <p>
        - It outlines regulations and penalties for smuggling, duty evasion, and other
        customs-related offenses.
      </p>
      <p>
        - The Act also provides for the temporary importation of personal effects and exemptions
        from duties for certain goods.
      </p>
      <h4 class="py-3 font-bold">Implications:</h4>
      <p>
        - The Act is expected to boost revenue generation, improve trade facilitation, and enhance
        the overall efficiency of customs operations in Nigeria.
      </p>
      <p>
        - It will also promote a safe environment for innovation and creativity, while ensuring
        compliance with international best practices.
      </p>
